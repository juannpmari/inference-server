def build_prefix_for_group(group: dict, group_idx: int = 0) -> list[dict]:
    """Build chat-message prefix for one prefix group, padded to ~2048 tokens."""
    rag_block = "\n\n---\n\n".join(
        f"### {doc['title']}\n\n{doc['body']}" for doc in group["docs"]
    )

    # Pad with deterministic appendix paragraphs until we hit the target.
    # Each group picks a different starting offset to keep prefixes unique.
    padding_parts = []
    current_chars = len(SYSTEM_PROMPT) + len(rag_block) + 200  # overhead
    idx = group_idx
    while current_chars < TARGET_PREFIX_CHARS:
        para = _FILLER_PARAGRAPHS[idx % len(_FILLER_PARAGRAPHS)]
        padding_parts.append(para)
        current_chars += len(para)
        idx += 1

    padding_block = ""
    if padding_parts:
        padding_block = "\n\n---\n\n" + "\n\n".join(padding_parts)

    context_message = (
        "The following internal engineering documents have been retrieved "
        "for reference. Use them to answer the user's question.\n\n"
        f"{rag_block}{padding_block}"
    )
    return [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": context_message},
        {
            "role": "assistant",
            "content": (
                "I have reviewed the retrieved documents. Please ask your question "
                "and I will answer based on the information provided."
            ),
        },
    ]


def build_prompts(num_groups: int = None) -> dict:
    """Build the full prompt dataset with multiple prefix groups."""
    groups = PREFIX_GROUPS[:num_groups] if num_groups else PREFIX_GROUPS

    # Round-robin ordering: g0q0, g1q0, g2q0, ..., g7q0, g0q1, g1q1, ...
    # This ensures consecutive requests always come from DIFFERENT groups,
    # so cache pressure scales smoothly with concurrency rather than
    # hitting a discrete threshold at group-size boundaries.
    by_qidx: dict[int, list[dict]] = {}
    prefix_stats = []

    for gid, group in enumerate(groups):
        prefix = build_prefix_for_group(group, group_idx=gid)
        prefix_chars = sum(len(m["content"]) for m in prefix)
        prefix_stats.append({"group": gid, "chars": prefix_chars,
                             "est_tokens": prefix_chars // 4})

        for qid, question in enumerate(group["questions"]):
            by_qidx.setdefault(qid, []).append({
                "group": gid,
                "messages": prefix + [{"role": "user", "content": question}],
                "question": question,
            })

    # Flatten round-robin: q0-g0, q0-g1, ..., q0-g7, q1-g0, q1-g1, ...
    prompts = []
    for qid in sorted(by_qidx):
        for entry in by_qidx[qid]:
            entry["id"] = len(prompts)
            prompts.append(entry)

    dataset = {
        "metadata": {
            "description": "APC vs LMCache experiment — multi-prefix groups (round-robin)",
            "num_prefix_groups": len(groups),
            "num_prompts": len(prompts),
            "prompts_per_group": len(groups[0]["questions"]),
            "target_prefix_tokens": "~2048 per group",
            "target_max_tokens": 128,
            "prefix_stats": prefix_stats,
        },
        "prompts": prompts,
    }
    return dataset


